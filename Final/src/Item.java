public class Item {
    public String name;
    public String tag;
    public long price;
    public int id;
    public String owner;

    public Item(String name, String tag, long price, int id, String owner) {
        this.name = name;
        this.tag = tag;
        this.price = price;
        this.id = id;
        this.owner = owner;
    }
}
